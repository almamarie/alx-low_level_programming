#include "main.h"
#include <string.h>
#include <stdio.h>
/**
 * _strcpy - entry point
 * Return: a pointer to an array
 * description: concatenates a string
 * @dest: destination of the concat
 * @src: source of the concatenation
*/

char *_strcpy(char *dest, char *src)
{
strcpy(dest, src);

return (dest);
}
